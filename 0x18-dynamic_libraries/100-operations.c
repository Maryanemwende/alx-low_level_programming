#include <stdio.h>

/**
 * add - adds two numbers
 * @a: first integer
 * @b: second integer
 * Return: the addition of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: first int
 * @b: second int
 * Return: the subtraction of a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: num1
 * @b: num2
 * Return: the multiplication of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: num1
 * @b: num2
 * Return: the resulting division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns the modulus (remainder) after division
 * @a: num1
 * @b: num2
 * Return: the remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
